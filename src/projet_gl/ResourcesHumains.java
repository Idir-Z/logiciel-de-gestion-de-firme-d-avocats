        // TODO add your handling code here:
        int reponse = JOptionPane.showConfirmDialog(rootPane, "voulez-vous vraiment supprimer cette avocat?", "confirmation de suppression", JOptionPane.YES_NO_OPTION);
        if(reponse == JOptionPane.YES_OPTION){
        int[] tuplesSelectione = tableAvocat.getSelectedRows();
        Connection connection = null;
       
            try{
        connection = DBConnection.etablirConnection();
        for(int i: tuplesSelectione){
            String deleteQuery = "DELETE FROM `avocat` WHERE idAvocat = ?";
            PreparedStatement preparedStatement = connection.prepareStatement(deleteQuery);
            int id = Integer.parseInt((String)tableAvocat.getValueAt(i, 0)); // Assuming ID is in the first column
            preparedStatement.setLong(1, id);
            preparedStatement.executeUpdate();
            
        }}catch(SQLException e){
            e.printStackTrace();
        }
            try {
                connection.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        
        jButton2ActionPerformed(evt);}
        
        
