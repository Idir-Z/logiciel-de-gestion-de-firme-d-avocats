    private void boutonAjoutDepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boutonAjoutDepActionPerformed
        // TODO add your handling code here:
        DepartementInputPan p = DepartementInputPan.ouvrirFenetre();
        int resultat = JOptionPane.showConfirmDialog(this, p, "Saisissez le nom du Département",
                        JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);
        if (resultat == JOptionPane.OK_OPTION){
            String libelle = p.getLibelle();
            control.Departement d = new control.Departement(libelle);
            int message = control.Departement.AjouterDepartement(d);
            switch (message) {
                case (control.Constants.AJOUTE_AVEC_SUCCEE):
                    JOptionPane.showMessageDialog(this, "Ajoute avec succes", null, JOptionPane.INFORMATION_MESSAGE);
                    AjoutSecreataire.remplirComboBoxDept();
                    
                    break;
                case (control.Constants.PROBLEME_DE_CONNEXION_BDD):
                JOptionPane.showMessageDialog(this, "Probleme de connexion à la BDD", null, JOptionPane.ERROR_MESSAGE);
                break;
                case (control.Constants.ELEMENT_DEJA_DANS_BDD):
                JOptionPane.showMessageDialog(this, "Le département que vous essayez d'ajouter est déjà dans la base de données", null, JOptionPane.ERROR_MESSAGE);
                break;
                default:
                    throw new AssertionError();
            }
        }
        
        
    }//GEN-LAST:event_boutonAjoutDepActionPerformed

